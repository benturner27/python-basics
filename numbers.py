#Basic mathematical operators

#Addition
a = 1 + 1
print(a)

#Subtraction
b = 2 - 1
print(b)

#Multiplication
c = 2 * 2
print(c)

#Division
d = 4 / 2
print(d)

#Modulo
e = 7 % 3
print(e)

#Power multiplication
f = 2 ** 3
print(f)

#Order of operations
g = 2 + 5 * 10 + 4
print(g)

#Order of operations (Brackets taking precedent)
h = (2 + 5) * (10 + 4)
print(h)