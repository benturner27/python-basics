#Booleans
#Are operators that convey true or false statements
#Important when dealing with control flow and logic
#When defining booleans, true and false statements need to be capitalised

#An example of a False statement
a = 1 > 2
print(a)

#An example of a True statement
b = 1 == 1
print(b)

#In Python, None is also available to use, as a way of a placeholder for a boolean.
c = None
print(c)